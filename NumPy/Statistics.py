import numpy as np

##Getting statistics of an array:

a = np.array([[1,2,3,4],[5,6,7,8]])
print(a)

##Gettting maximum value in the array

print(np.max(a))
#OUTPUT  >>>>  8

##Getting minimum value in the array

print(np.min(a))
#OUTPUT  >>>>> 1

##getting the sum total of the elements in the array
print(np.sum(a))
##Output  >>>> 36
